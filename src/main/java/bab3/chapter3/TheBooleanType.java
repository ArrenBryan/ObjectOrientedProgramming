package bab3.chapter3;

public class TheBooleanType {
    public static void main(String[] args) {
        boolean sayaBahagia = true;
        
        if (sayaBahagia = true)
        {
            System.out.println("Mamah senang");
        }
        
        if (sayaBahagia = false)
        {
            System.out.println("Mamah tidak senang");
        }
    }
}

